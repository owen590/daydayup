// 生成汉字数据
const generateHanziData = (): LearningItem[] => {
  return COMMON_HANZI.map((hanzi, index) => {
    // 分类逻辑
    let subcategory: string;
    
    if (index < 100) {
      subcategory = 'numbers';
    } else if (index < 300) {
      subcategory = 'nature';
    } else if (index < 600) {
      subcategory = 'people';
    } else if (index < 800) {
      subcategory = 'school';
    } else if (index < 900) {
      subcategory = 'directions';
    } else if (index < 1200) {
      subcategory = 'adjectives';
    } else if (index < 1800) {
      subcategory = 'verbs';
    } else {
      subcategory = 'common';
    }
    
    return {
      id: `h${index + 1}`,
      term: hanzi,
      pronunciation: generatePinyin(hanzi),
      meaning: generateMeaning(hanzi),
      example: generateExample(hanzi),
      category: 'hanzi',
      subcategory: subcategory,
      // 前2500个为会写字，符合小学要求
      isWritable: index < 2500
    };
  });
};

